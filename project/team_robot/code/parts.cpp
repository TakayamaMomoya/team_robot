//=====================================================
// 生成処理
//=====================================================
CParts *CParts::Create(void)
{
	CParts *pParts = nullptr;

	if (pParts == nullptr)
	{
		// インスタンス生成
		pParts = new CParts;

		if (pParts != nullptr)
		{
			// 初期化処理
			pParts->Init();
		}
	}

	return pParts;
}

//====================================================
// 初期化処理
//====================================================
HRESULT CParts::Init(void)
{
	CObjectX::Init();

	return S_OK;
}

//====================================================
// 終了処理
//====================================================
void CParts::Uninit(void)
{
	CObjectX::Uninit();
}

//====================================================
// 更新処理
//====================================================
void CParts::Update(void)
{
	CObjectX::Update();

	// 前回のマトリックスを保存
	m_mtxWorldOld = GetMatrix();
}

