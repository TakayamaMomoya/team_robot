//=====================================================
// 終了処理
//=====================================================
void CAlly::Uninit(void)
{
	CMobFactory *pMobfactory = CMobFactory::GetInstance();

	if (pMobfactory != nullptr)
	{
		list<CMob*> aAlly = pMobfactory->GetAlly();

		aAlly.remove((CMob*)this);

		pMobfactory->SetAlly(aAlly);
	}

	CMob::Uninit();
}

