//=====================================================
// 終了処理
//=====================================================
void CEnemy::Uninit(void)
{
	CMobFactory *pMobfactory = CMobFactory::GetInstance();

	if (pMobfactory != nullptr)
	{
		list<CMob*> aEnemy = pMobfactory->GetEnemy();

		aEnemy.remove((CMob*)this);

		pMobfactory->SetEnemy(aEnemy);
	}

	CMob::Uninit();
}

