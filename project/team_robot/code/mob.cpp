//=====================================================
// 生成処理
//=====================================================
CMob *CMob::Create(void)
{
	CMob *pMob = nullptr;

	pMob = new CMob;

	if (pMob == nullptr)
		return nullptr;

	pMob->Init();

	return pMob;
}

//=====================================================
// 初期化処理
//=====================================================
HRESULT CMob::Init(void)
{
	// 継承クラスの初期化
	CMotion::Init();

	Load("data\\MOTION\\motionArms01.txt");

	SetMotion(0);

	return S_OK;
}

